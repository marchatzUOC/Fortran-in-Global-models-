TYPE :: TEAERC_DUST
!- based on SURFACE 2D (lat,lon) 1x1
REAL(KIND=JPRB), ALLOCATABLE, DIMENSION(:,:)   :: CULT,POTSRC
REAL(KIND=JPRB), ALLOCATABLE, DIMENSION(:,:)   :: AREA
REAL(KIND=JPRB), ALLOCATABLE, DIMENSION(:,:)   :: SOILPH1,SOILPH2,SOILPH3,SOILPH4,SOILPH5
REAL(KIND=JPRB), ALLOCATABLE, DIMENSION(:,:)   :: LAI_MAX, LAI_AVG
REAL(KIND=JPRB), ALLOCATABLE, DIMENSION(:,:)   :: Z0AM
REAL(KIND=JPRB), ALLOCATABLE, DIMENSION(:,:)   :: SOILTYPE

!latitude and logitude in Degrees 
REAL(KIND=JPRB),ALLOCATABLE, DIMENSION(:)     :: LATD,LOND

! in radians
REAL(KIND=JPRB) :: LAT1 = 0.0_JPRB, LON1 = 0.0_JPRB
REAL(KIND=JPRB) :: DLAT = 0.0_JPRB, DLON = 0.0_JPRB
INTEGER(KIND=JPIM) :: NLAT = 0, NLON = 0

REAL(KIND=JPRB), ALLOCATABLE, DIMENSION(:,:,:) :: Z0M
REAL(KIND=JPRB), ALLOCATABLE, DIMENSION(:,:,:) :: FPAR
!lon,lat,month

! Number of the Grouped fields for 2d-3d, numb-month
INTEGER(KIND=JPIM) :: NMONTH=0

! INTERPOLATED ARRAYS MAYBE DO NOT NEED IT but saved into the main  pointer
REAL(KIND=JPRB),ALLOCATABLE, DIMENSION(:)   :: TCULT, TSOILTYPE, TPOTSRC
REAL(KIND=JPRB),ALLOCATABLE, DIMENSION(:)   :: TAREA,  TZ0AM
REAL(KIND=JPRB),ALLOCATABLE, DIMENSION(:)   :: TSOILPH1, TSOILPH2, TSOILPH3, TSOILPH4, TSOILPH5
REAL(KIND=JPRB),ALLOCATABLE, DIMENSION(:)   :: TZ0M , TFPAR
REAL(KIND=JPRB),ALLOCATABLE, DIMENSION(:)   :: TLAI_MAX, TLAI_AVG
LOGICAL :: IS_INITIALISED = .FALSE.
LOGICAL :: IS_INTERPOLATED = .FALSE.

CONTAINS

PROCEDURE :: READ => SETUP_DUST_CLIM
PROCEDURE :: INTERPOLATION =>SU_DUST_FIELDS_INTERP
PROCEDURE :: FINALIZE => CLEANUP_DUST_CLIM 

END TYPE TEAERC_DUST

